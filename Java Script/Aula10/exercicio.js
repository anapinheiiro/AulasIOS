//Adição
function adição(num1, num2){
    return (num1 + num2)
}

//Subtração
function Subtração(num1, num2){
    return (num1 - num2)
}


//Multiplicação
function Multiplicação(num1, num2){
    return (num1 * num2)
}


// Divisão
function Divisão(num1, num2) {
    return (num1 / num2)
}

console.log(`Adição: ${adição(20, 98)} `);
console.log(`Subtração: ${Subtração(10, 1)}`);
console.log(`Mltiplicação: ${Multiplicação(10, 10)}`);
console.log(`Divisão: ${Divisão(20, 98)} `);